// Variables and Datatypes
/*
    DataTypes => typeof (datatype)
    1) NULL.
    2) Undefined
    3) Boolean
    4) number
    5) string
    6) OBJECTs

    Variable keywords
    var, let, const
    */

var x = 10;
console.log(x); 
console.log("Type of x: " + typeof x);

x = "Islam";
console.log(x);
console.log("Type of x: " + typeof x);

x = false;
console.log(x);
console.log("Type of x: " + typeof x);

x = null;
console.log(x);  // object
console.log("Type of x: " + typeof x);

// Var is dynamically typed keyword used for declaring and defining variable




